package Entities;

public class Product {
	
	String prod;
	double value;
	
	public Product(String prod, double value) {
		this.prod = prod;
		this.value = value;
	}

	public String getProd() {
		return prod;
	}

	public void setProd(String prod) {
		this.prod = prod;
	}

	public double getValue() {
		return value;
	}

	public void setValue(double value) {
		this.value = value;
	}
	
	
	
	

}
